package com.TallyMarks.GrantsManagementInformationSystem.Dto;

public class ExportMaterialsDto {

	private int id;
	private String name;
	private String exportMaterials;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getExportMaterials() {
		return exportMaterials;
	}

	public void setExportMaterials(String exportMaterials) {
		this.exportMaterials = exportMaterials;
	}

}
