package com.TallyMarks.GrantsManagementInformationSystem.Dto;

public class LocalMaterialsDto {

	private int id;
	private String name;
	private String localMaterials;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getLocalMaterials() {
		return localMaterials;
	}

	public void setLocalMaterials(String localMaterials) {
		this.localMaterials = localMaterials;
	}

}
