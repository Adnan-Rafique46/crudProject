package com.TallyMarks.GrantsManagementInformationSystem.Dto;

public class ExportMarketDto {

	private int id;
	private String name;
	private String exportMarket;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getExportMarket() {
		return exportMarket;
	}

	public void setExportMarket(String exportMarket) {
		this.exportMarket = exportMarket;
	}

}
